def write_excel(items, path):
    """テスト項目をExcelに書き出す"""
    wb = openpyxl.Workbook()
    ws = wb.active
    ws.title = "基本機能テスト項目"

    # ヘッダ定義
    headers = ["No", "確認項目", "目的", "外部入力", "マイコン入力判定",
               "出力確認対象", "判定基準", "根拠節", "入力", "出力"]

    # スタイル
    header_font = Font(bold=True, size=10)
    header_fill = PatternFill(start_color="4472C4", end_color="4472C4", fill_type="solid")
    header_font_white = Font(bold=True, size=10, color="FFFFFF")
    cell_font = Font(size=9)
    thin_border = Border(
        left=Side(style='thin'), right=Side(style='thin'),
        top=Side(style='thin'), bottom=Side(style='thin')
    )
    wrap_align = Alignment(wrap_text=True, vertical='top')

    # ヘッダ書き込み
    for col_idx, h in enumerate(headers, 1):
        cell = ws.cell(row=1, column=col_idx, value=h)
        cell.font = header_font_white
        cell.fill = header_fill
        cell.border = thin_border
        cell.alignment = Alignment(horizontal='center', vertical='center')

    # データ書き込み
    for row_idx, item in enumerate(items, 2):
        for col_idx, key in enumerate(headers, 1):
            cell = ws.cell(row=row_idx, column=col_idx, value=item.get(key, ""))
            cell.font = cell_font
            cell.border = thin_border
            cell.alignment = wrap_align

    # 列幅設定
    col_widths = [5, 30, 40, 40, 35, 25, 40, 25, 18, 18]
    for i, w in enumerate(col_widths):
        ws.column_dimensions[chr(65 + i)].width = w

    # フィルタ設定
    ws.auto_filter.ref = f"A1:J{len(items)+1}"

    # フリーズペイン
    ws.freeze_panes = "B2"

    wb.save(path)
    return len(items)


